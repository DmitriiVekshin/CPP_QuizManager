/*!
 \file implementation part of classes CCursor
 */
#include "CCursor.h"

//=============================================================================================================================

                                            CCursor::CCursor                                ( void )
                                            {
                                            }
//=============================================================================================================================

                                            CCursor::~CCursor                               ( void       )
                                            {
                                            this->DelCursor();    
                                            }
//=============================================================================================================================

    void                                    CCursor::SetX                                   ( int    x   )
    {
        m_X = x;
    }
//=============================================================================================================================
    
    void                                    CCursor::SetY                                   ( int    y   )
    {
        m_Y = y;
    }
//=============================================================================================================================

    int                                     CCursor::GetX                                   ( void       ) const
    {
        return m_X;
    }
//=============================================================================================================================

    int                                     CCursor::GetY                                   ( void       ) const
    {
        return m_Y;
    }
//=============================================================================================================================
/*!
    Draw cursor
*/
    void                                    CCursor::DrawCursor                             ( void ) const
    {
        refresh();
        
        char cursorChar=(char) 62;

        move(m_Y,m_X);
        addch(cursorChar);
        
        refresh();
    
    }
//=============================================================================================================================
/*!
    Delete cursor
*/
    void                                    CCursor::DelCursor                              ( void ) const
    {
        refresh();
        
        char cursorClearChar=(char) 32;

        move(m_Y,m_X);
        addch(cursorClearChar);
        
        refresh();
    
    }
//=============================================================================================================================
/*!
    Move cursor
*/
    void                                    CCursor::MoveCursor                             ( MenuCommand & command )
    {
            this->DelCursor();
            switch(command)
            {
                case MenuCommand::Up :
                        m_Y -= 2;
                    break;
                case MenuCommand::Down :
                        m_Y += 2;
                    break;
                default:
                    break;                                               
            }
    }
